.MODEL SMALL
 
.STACK 100H

.DATA 
MSG1 DB "Enter two Numbers :$"  
MSG2 DB "SUM: $"
MSG3 DB " Division: $"
MSG4 DB " 1 OR 2 $"
.CODE 
MAIN PROC 

;initialize DS

MOV AX,@DATA 
MOV DS,AX 

XOR AX,AX

LEA DX,MSG1
MOV AH,9
INT 21H

MOV AH,1 
INT 21H 
XOR BX,BX
MOV BL,AL
SUB BL,30H

MOV AH,1  
INT 21H
XOR CX,CX
MOV CL,AL
SUB CL,30H
 
MOV AH,2
MOV DL,0DH
INT 21H
MOV DL, 0AH
INT 21H

LEA DX,MSG4
MOV AH,9 
INT 21H

MOV AH,1   
INT 21H
XOR CH,CH
MOV CH,AL
SUB CH,30H 

MOV AH,2
MOV DL,0DH
INT 21H
MOV DL, 0AH
INT 21H

CMP CH,1H
JE ADDITION
CMP CH,2H
JE DIVISION
JMP AFTER_LIFE

ADDITION:
    LEA DX,MSG2
    MOV AH,9 
    INT 21H
    ADD BL,CL
    ADD BL,30H
  
    MOV AH,2
    MOV DL,BL
    INT 21H
    
    JMP AFTER_LIFE 
    
DIVISION:
    LEA DX,MSG3
    MOV AH,9 
    INT 21H
    XOR BH,BH
    XOR AX,AX
    MOV AX,BX
    DIV CL
    
    ADD AL,30H 
    
    MOV AH,2
    MOV DL,AL
    INT 21H
    
    JMP AFTER_LIFE 



;exit to DOS 
AFTER_LIFE: ;EXIT LEVEL              
MOV AX,4C00H
INT 21H 

MAIN ENDP
    END MAIN 